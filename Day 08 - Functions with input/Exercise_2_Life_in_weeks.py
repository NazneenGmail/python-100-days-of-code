def life_in_weeks(age):
    age_in_years = 80 - age
    age_in_weeks = age_in_years * 52
    print(f"You are {age} years old. You have {age_in_weeks} weeks left in life.")

life_in_weeks(1)
life_in_weeks(4)
life_in_weeks(5)
life_in_weeks(10)
life_in_weeks(40)
life_in_weeks(41)
life_in_weeks(64)
life_in_weeks(70)